



$(function(){
  var figure=$('#sub01-c02 div.inner div.first .box:nth-child(1)');
  var hoverbox=$('#sub01-c02 div.inner div.first .box:nth-child(1) .hover');

  figure.mouseenter(function(){
      hoverbox.stop().animate({opacity: "1"});
  }).mouseleave(function(){
    hoverbox.stop().animate({opacity:"0"})
  })
});


